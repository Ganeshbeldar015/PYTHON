# Arithmatic Operators ( +,-,*,/,%,**)

a=5
b=2

print(a + b) 
print(a - b) 
print(a * b) 
print(a / b) 
print(a % b)  #remainder
print(a ** b)  # a^b

# Relational/comaparison operators (==,!=,>,<,>=,<=)
    #OUTPUT = (True/False)
a=50
b=20

print(a == b) #Equality
print(a != b) #Not Equality
print(a > b) #Greater Than 
print(a < b) #Less Than
print(a >= b) #Greater Than or Equal to
print(a <= b) #Less Than or Equal to

