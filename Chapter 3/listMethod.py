my_list = [2, 1, 3]
my_list.append(4)  # Adds one element at the end
print(my_list)

my_list.sort()  # Sorts in ascending order
print(my_list)

my_list.sort(reverse=True)  # Sorts in descending order
print(my_list)

my_list.reverse()  # Reverses the list
print(my_list)

my_list.insert(2, 5)  # Inserts 5 at index 2
print(my_list)

my_list.remove(2)  # Removes the first occurrence of 2
print(my_list)

my_list.pop(0)  # Removes element at index 0
print(my_list)
